    private void cargarListaClientes() {
        clienteComboBox.removeAllItems();
        clienteComboBox.addItem(null);
        for (Persona p : new RolPersonaControlador().leerLista(Rol.RECOLECTOR)) {
            clienteComboBox.addItem(p);
        }
    }
