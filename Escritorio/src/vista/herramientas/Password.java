    public static String leerArchivoContraseña() {
        File archivo = null;
        FileReader fr = null;
        BufferedReader br = null;
        String linea = null;
        try {
            archivo = new File("data/password.pwd");
            fr = new FileReader(archivo);
            br = new BufferedReader(fr);

            // Lectura del fichero
            linea = br.readLine();
        } catch (Exception e) {
        } finally {
            try {
                fr.close();
            } catch (IOException ex) {
                Logger.getLogger(Password.class.getName()).log(Level.SEVERE, null, ex);
            }
            return linea;
        }
    }

    private void cambiarContraseña(String text) {
        FileWriter fichero = null;
        PrintWriter pw;
        try {
            fichero = new FileWriter("data/password.pwd");
            pw = new PrintWriter(fichero);
            pw.println(text);

        } catch (Exception e) {
            e.printStackTrace();
        } finally {
            try {
                if (null != fichero) {
                    fichero.close();
                }
            } catch (Exception e2) {
                e2.printStackTrace();
            }
        }
    }
