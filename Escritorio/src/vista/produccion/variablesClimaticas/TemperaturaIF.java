    private void cargarTablaPrincipal() {
        while (principalTable.getRowCount() > 0) {
            ((DefaultTableModel) principalTable.getModel()).removeRow(0);
        }
        Date esteMes = DateTools.getDate((int) añoSpinner.getValue(), (int) mesComboBox.getSelectedIndex(), 1);
        Calendar c = GregorianCalendar.getInstance();
        c.setTime(esteMes);
        c.add(Calendar.MONTH, 1);
        c.add(Calendar.DAY_OF_MONTH, -1);
        Date siguienteMes = c.getTime();
        lista = controlador.buscarLista(esteMes, siguienteMes);
        for (Temperatura t : lista) {
            Object[] row = {DateFormatter.formatDate(t.getFecha()),
                DateFormatter.formatTime(t.getHora()),
                t.getTemperatura(),
                t.getHumedad(),
                t.getPuntoDeRocio()};
            ((DefaultTableModel) principalTable.getModel()).addRow(row);
        }
    }

    private void cargarTablaDePromedios() {
        for (int i = 0; i < 12; i++) {
            calcularPromedioMes(i);
        }
    }

    private void calcularPromedioMes(int i) {
        Date fecha1 = DateTools.getDate((int) añoSpinner.getValue(), i, 1);
        Calendar c = GregorianCalendar.getInstance();
        c.setTime(fecha1);
        c.add(Calendar.MONTH, 1);
        c.add(Calendar.DAY_OF_MONTH, -1);
        Date fecha2 = c.getTime();
        List<Temperatura> listaMes = controlador.buscarLista(fecha1, fecha2);
        if (listaMes.size() > 0) {
            float p1 = 0;
            for (Temperatura t : listaMes) {
                p1 += t.getTemperatura();
            }
            p1 = p1 / listaMes.size();
            promediosMesTable.getModel().setValueAt(p1, i, 1);
        } else {
            promediosMesTable.getModel().setValueAt("-", i, 1);
        }
    }
